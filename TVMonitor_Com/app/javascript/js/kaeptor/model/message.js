/*******************************************************************************
 * Message Class
 */
function KaeptorMessage(){     
    
    /**************************************************************************\ 
    | Private:
    \**************************************************************************/ 
    var id;
    var body;
    
    /**************************************************************************\ 
    | Constructor:
    \**************************************************************************/
    this.constructor.id    = "";
    this.constructor.body  = "";
    
    /**************************************************************************\ 
    | Privileged Methods:
    \**************************************************************************/
    //Getters
    this.getId    = function(){
        return id;
    };
    this.getBody  = function(){
        return body;
    };
    //Setters
    this.setId    = function( idVal ){
        id = idVal;
    };
    this.setBody  = function( bodyVal ){
        body = bodyVal;
    };
    
    //Controller
    this.get = function(){};
    
} 

